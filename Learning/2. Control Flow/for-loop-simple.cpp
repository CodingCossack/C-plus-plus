// Program to print numbers 5 to 1 using for loop
// for (initialization; boolean_expression; update)

#include <iostream>
using namespace std;

int main() {

    for (int i = 5; i > 0 ; i--) {		// run a for loop from i = 5 to 1
        cout << i << endl;				// print the value of i
    }			
    
    return 0;
}
